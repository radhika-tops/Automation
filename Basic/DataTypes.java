package Basic;
//two types
//1.Primitive - byte,short,int,long,float,double,char,boolean
//2.Non-primitive - String ,Array ,Class
public class DataTypes {
	public static void main(String[] args) {
		System.out.println("Hello datatypes");
		byte b = 126;
		short s = 23;
		int i = 123;
		long l = 1278645;
		System.out.println("Value of byte = " +b);
		System.out.println("Value of short = " +s);
		System.out.println("Value of int = " +i);
		System.out.println("Value of long = " +l);
		float f = 3.1456789f;
		System.out.println("Value of float = " +f);
		double d = 3.14567;
		System.out.println("Value of double = " +d);
		char c = 'q';
		System.out.println(c);
		boolean b1 = true;
		String s1 = "Hello Java";
		System.out.println(s1);
	}

}
